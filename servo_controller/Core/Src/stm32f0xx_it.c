/**
  * @brief This function togles pins PC8 and PC9 (green and orange LEDs)
  */
void toggle_leds(void)
{
	if (GPIOC->ODR & (1 << 8)) {
		GPIOC->ODR &= ~(1 << 8);
		GPIOC->ODR |= (1 << 9);
	}
	else if (GPIOC->ODR & (1 << 9)) {
		GPIOC->ODR &= ~(1 << 9);
		GPIOC->ODR |= (1 << 8);
	}
}
